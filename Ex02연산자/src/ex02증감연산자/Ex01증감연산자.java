package ex02증감연산자;

public class Ex01증감연산자 {

	public static void main(String[] args) {
		int num = 3;
		
		// 증감연산자 : 위치에 따라서 증가되는 순서가 변동
		// ++변수 >> 1 증가 >> 변수 사용
		// 변수++ >> 변수 사용 >> 1 증가
		System.out.println(++num);
		System.out.println(num);
		System.out.println(num++);
		System.out.println(num);
		
	}

}
