package doWhile문;

public class Ex01doWhile기초 {

	public static void main(String[] args) {
		// do-while문 기초
		// while문, do-while문
		// : 반복횟수가 명확하지 않을 때 주로 사용
		// do-while문은 반드시 한번은 실행해야하는 코드가 있을 때 많이 사용!
		// : 검사조건이 false여도 한번은 실행된다!
		do {
			System.out.println("반드시 한번은 실행");
		}while(false);

	}

}
