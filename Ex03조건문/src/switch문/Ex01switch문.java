package switch문;

public class Ex01switch문 {

	public static void main(String[] args) {
		
		switch (3/2) {
		case 1 : 
			System.out.println("실행문1");
			break;
		case 2:
			System.out.println("실행문2");
			break;
		default : 
			System.out.println("실행문3");
			break;
		
		}

	}

}
