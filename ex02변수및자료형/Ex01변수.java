package ex02변수및자료형;

public class Ex01변수 {
	
	public static void main(String[] args) {
		
		// 변수 선언
		// 정수 데이터 7이라는 값을 담을 수 있는 num이라는 변수 선언
		int num = 7;
		System.out.println(num);
		
		// num이라는 공간에 있는 데이터를 3으로 변경
		num = 3;
		System.out.println(num);
		
		// 상수 선언 방법
		// 정수형 2라는 숫자를 담을 수 있는 상수 num2를 선언
		final int num2 = 2;
		System.out.println(num2);
		
		// num2 = 5; ---> 상수는 값 변경 불가능!
		
		// 변수명 규칙
		// 1. 키워드 사용 불가 (int, final...)
		// 2. 대소문자 구분 O, 길이 제한 X
		// 3. 숫자로 시작할 수 없다.
		// 4. 특수문자는 _와 $만 허용
		
		// 변수명 권장 규칙
		// 1. 첫글자는 항상 소문자로 시작
		// 2. 변수 이름이 여러 단어일 경우 이어지는 첫글자는 대문자로 한다.
		// ex) int wishList
	}
	
		
	}

